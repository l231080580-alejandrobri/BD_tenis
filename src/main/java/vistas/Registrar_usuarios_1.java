    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed

    int filaSeleccionada = tb_lista_usuarios.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un usuario", "Sin selección", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    try {
        int idUsuario = Integer.parseInt(tb_lista_usuarios.getValueAt(filaSeleccionada, 0).toString());
        
        crud_usuarios crud = new crud_usuarios();
        if (crud.eliminar_Usuario(idUsuario)) {
            // LIMPIAR TABLA
            DefaultTableModel modelo = (DefaultTableModel) tb_lista_usuarios.getModel();
            //((DefaultTableModel) tb_lista_usuarios.getModel()).setRowCount(0);
            modelo.removeRow(filaSeleccionada);

            // LIMPIAR CAMPOS
            txtid_usuario.setText("");
            txtnombre.setText("");
            txtapellido.setText("");
            txtcorreo.setText("");
            txtcontraseña.setText("");
            txttelefono.setText("");
            txtrol.setText("");
            txtfecha_registro.setText("");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al eliminar", "Error", JOptionPane.ERROR_MESSAGE);
    }
   // TODO add your handling code here:
    }//GEN-LAST:event_btneliminarActionPerformed

