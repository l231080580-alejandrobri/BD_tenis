    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed

    String nombre = txtnombre.getText();
    String apellido = txtapellido.getText();
    String correo = txtcorreo.getText();
    String contraseña = txtcontraseña.getText();
    String telefono = txttelefono.getText();
    String rol = txtrol.getText();
    //String fecha_registro = txtfecha_registro.getText();
String fecha_registro = txtfecha_registro.getText();


    
    if(nombre.isEmpty() || apellido.isEmpty() || correo.isEmpty() || contraseña.isEmpty() || telefono.isEmpty() || rol.isEmpty() || fecha_registro.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    crud_usuarios objmostrar= new crud_usuarios();
    objmostrar.Mostrar_usuarios(tb_lista_usuarios);
    
    if(crud.insertar_Usuario(nombre, apellido, correo, contraseña, telefono, rol, fecha_registro)) {
        txtid_usuario.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
        txtcorreo.setText("");
        txtcontraseña.setText("");
        txttelefono.setText("");
        txtrol.setText("");
        txtfecha_registro.setText("");
        
    objmostrar.Mostrar_usuarios(tb_lista_usuarios);
    }
 
    }//GEN-LAST:event_btnagregarActionPerformed

